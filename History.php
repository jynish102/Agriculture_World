<p><h1>The farming Revolution</h1>
     
  Taking root around 12,000 years ago, agriculture triggered such a change in society and the way in which people lived that its development has been dubbed the <mark>Neolithic Revolution.</mark> Traditional hunter-gatherer lifestyles, followed by humans since their evolution, were swept aside in favor of permanent settlements and a reliable food supply. Out of agriculture, cities and civilizations grew, and because crops and animals could now be farmed to meet demand, the global population rocketed from some five million people 10,000 years ago, to eight billion today.
