            <p>There are multiple schemes that provide subsidies for agricultural machinery in India, including:
Sub-mission On Agriculture Mechanization (SMAM)
This central government scheme offers subsidies ranging from 50–80% for buying agricultural machinery. Women farmers are given priority. 
Financial Support Scheme for Farm Mechanization (FSSM)
This scheme offers a subsidy of 50–60% of the machinery's indicative price, up to a maximum of ₹3,00,000. 
Pradhan Mantri Kisan Tractor Yojana
This scheme offers a 50% subsidy on buying tractors. Farmers can apply for the scheme and the subsidy amount is sent to their account. </p>

