      <p>The government provides various subsidies to farmers in India to support the agricultural sector, including:</p>
         <ol>
           <li>Input subsidies: These subsidies are provided for items like seeds, fertilizers, and electricity to reduce costs.<a href="https://www.cwejournal.org/article/poverview-of-agricultural-subsidies-in-india-and-its-impact-on-environmentp"><mark><i class="fa-solid fa-link"></i></mark></a></li>
           <li>Minimum support price (MSP) mechanisms: The government sets a minimum price for farm products. 
            <a href="https://www.cwejournal.org/article/poverview-of-agricultural-subsidies-in-india-and-its-impact-on-environmentp"><mark><i class="fa-solid fa-link"></i></mark></a></li>
           <li>Concessional trade policies: The government provides policies for the import and export of farm products.
           </li>
           <li>Direct income transfers: The government provides direct income transfers to farmers.
           </li>
           <li>Financial assistance: The government provides financial assistance for the distribution of certified or foundation seeds.
           </li>
           <li>PM-Kisan Samman Nidhi: The government provides a scheme called PM-Kisan Samman Nidhi.
           </li> 
      <p>
        Other schemes that provide support to farmers include: National Food Security Mission (NFSM), Mission for Integrated Development of Horticulture (MIDH), Rashtriya Krishi Vikas Yojana (RKVY), and Paramparagat Krishi Vikas Yojana (PKVY). </p>
        <p><mark>Fertilizer, credit, irrigation and crop insurance </mark>are explicit subsidies in that the government provides budgetary heads to charge these expenditures.The <mark>Food Subsidy</mark> gets the maximum share in the total subsidy given by the Central Government of India.11</p>
        </ol>
        
