    <p>Gujarat offers subsidies for the purchase of tractors and power tillers under various schemes, including:</p>
<p><h2>AGR-50 Scheme</h2>
Farmers can receive financial assistance for tractor purchases, with the following benefits:</p>
<ol>
  <li>For tractors up to 40 PTO HP, farmers receive 25% of the expense or ₹45,000, whichever is less<a href="https://search.app?link=https%3A%2F%2Fwww.myscheme.gov.in%2Fschemes%2Fagr50s-apt&utm_campaign=aga&utm_source=agsadl1%2Csh%2Fx%2Fgs%2Fm2%2F4"> <mark><i class="fa-solid fa-link"></i></mark></a></li>

